#!/usr/bin/env python3
"""This is a type-annoted function that
takes  a float n as argument and returns
the floor of the float."""

import math


def floor(n: float) -> int:
    """This is a type-annoted function that
    takes  a float n as argument and returns
    the floor of the float.
    Args:
        n: a float object.
    Returns:
        The floor of the float."""
    return math.floor(n)
